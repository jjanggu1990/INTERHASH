package board;

public class AdminHashDataBean {
	private String HashName;
	private int HashLikeCount;
	private int HashContentCount;
	
	public String getHashName() {
		return HashName;
	}
	public void setHashName(String hashName) {
		HashName = hashName;
	}
	public int getHashLikeCount() {
		return HashLikeCount;
	}
	public void setHashLikeCount(int hashLikeCount) {
		HashLikeCount = hashLikeCount;
	}
	public int getHashContentCount() {
		return HashContentCount;
	}
	public void setHashContentCount(int hashContentCount) {
		HashContentCount = hashContentCount;
	}

}
