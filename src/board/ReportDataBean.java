package board;

public class ReportDataBean {
	String recontent;
	String recategory;
	char redistinction;
	int connum;
	String email;
	
	public String getRecontent() {
		return recontent;
	}
	public void setRecontent(String recontent) {
		this.recontent = recontent;
	}
	public String getRecategory() {
		return recategory;
	}
	public void setRecategory(String recategory) {
		this.recategory = recategory;
	}
	public char getRedistinction() {
		return redistinction;
	}
	public void setRedistinction(char redistinction) {
		this.redistinction = redistinction;
	}
	public int getConnum() {
		return connum;
	}
	public void setConnum(int connum) {
		this.connum = connum;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}

	
}
